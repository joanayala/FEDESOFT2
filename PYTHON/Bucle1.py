#Listar números del 1 al 10 en orden ascendente
import os

os.system("cls") # Clear console screen

print("::: Lista de números en orden ascendente :::")
i = 1
while i <= 10 :
    print(i)
    i = i + 1

print("::: Lista de números en orden descendente :::")
j = 10
while j >= 1 :
    print(j)
    j = j - 1
